package if_else_statements;

public class else_if_statements {
    public static void main(String[] args) {

        int maths = 12; 

        if (maths>=75)
        {
            System.out.println("A");
            
        }
        else if (maths >= 65)
        {
            System.out.println("B");
        }
        else if (maths>=55)
        {
            System.out.println("C");
        }
        else if (maths>=35)
        {
            System.out.println("S");
        }
        else if (maths<=34)
        {
            System.out.println("F");
        }
        
    }
    
}
