package math_operators;

public class increment_operator {
    public static void main(String[] args) {
        
        int number = 10;
        int number2 = 10;
        int number3 = 10;
        int number4 = 10;

        ++number; 
        --number;

        number += 10;
        number2 -= 10;
        number3 *= 10;
        number4 /= 10;

        System.out.println(number);
        System.out.println(number2);
        System.out.println(number3);
        System.out.println(number4);
    }
    
}
